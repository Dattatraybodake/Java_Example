// how to find the length of a string using the length() method.
package string;

// Class to finding the length of a string
public class Length_String {

    // Main method
    public static void main(String[] args) {
        String s = new String("Good Morning"); // String instance
        
        int l = s.length(); // Length of the string
        
        // Printing the length of the string
        System.out.println("Length of String is: " + l);
    }
}
