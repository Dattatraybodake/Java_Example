package collection_list_arraylist;

public class Player {

	private int id;
	private String name;
	private int Run;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getRun() {
		return Run;
	}
	public void setRun(int run) {
		Run = run;
	}
}